// 1. Create an array.
let months = ["Feb", "Mar", "Apr", "May"];

// 2. Display the array using console.log
console.log("Initial array:", months);

// 3. Display the array length
console.log("Array length:", months.length);

// 4. Display the first element in the array
console.log("First element:", months[0]);

// 5. Add the following value to the end (use push): "Jun"
months.push("Jun");

// 6. Display the array using console.log
console.log("Array after push:", months);

// 7. Display the array length
console.log("Array length after push:", months.length);

// 8. Display the last element in the array
console.log("Last element:", months[months.length - 1]);

// 9. Add the following element at the beginning of the array (use unshift): "Jan"
months.unshift("Jan");

// 10. Display the array using console.log
console.log("Array after unshift:", months);